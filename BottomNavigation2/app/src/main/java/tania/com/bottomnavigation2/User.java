package tania.com.bottomnavigation2;

public class User {
    private String name;
    private String phoneNo;

    public User(String name, String phoneNo) {
        this.name = name;
        this.phoneNo = phoneNo;
    }

    public String getName() {
        return name;
    }

    public String getPhoneNo() {
        return phoneNo;
    }
}
